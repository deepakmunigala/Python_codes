l = [0, 1, 2]

l.append(100)
print(l)
# [0, 1, 2, 100]

l.append('abc')
print(l)
# [0, 1, 2, 100, 'abc']

l.append([3, 4, 5])
print(l)
# [0, 1, 2, 100, 'abc', [3, 4, 5]]

l = [0, 1, 2]

l.extend([10, 11, 12])
print(l)
# [0, 1, 2, 10, 11, 12]

l.extend((100, 101, 102))
print(l)
# [0, 1, 2, 10, 11, 12, 100, 101, 102]

l.extend('abc')
print(l)
# [0, 1, 2, 10, 11, 12, 100, 101, 102, 'a', 'b', 'c']

l_new = l + [3, 4, 5]
print(l_new)
# [0, 1, 2, 10, 11, 12, 100, 101, 102, 'a', 'b', 'c', 3, 4, 5]

l += [3, 4, 5]
print(l)
# [0, 1, 2, 10, 11, 12, 100, 101, 102, 'a', 'b', 'c', 3, 4, 5]

l = ['a', 'b', 'c']

l.insert(1, 100)
print(l)
# ['a', 100, 'b', 'c']

l.insert(0, 200)
print(l)
# [200, 'a', 100, 'b', 'c']

l.insert(-1, 300)
print(l)
# [200, 'a', 100, 'b', 300, 'c']

l.insert(-2, 400)
print(l)
# [200, 'a', 100, 'b', 400, 300, 'c']

l.insert(0, [-1, -2, -3])
print(l)
# [[-1, -2, -3], 200, 'a', 100, 'b', 400, 300, 'c']

l = ['a', 'b', 'c']

l[1:1] = [100, 200, 300]
print(l)
# ['a', 100, 200, 300, 'b', 'c']

l = ['a', 'b', 'c']

l[1:2] = [100, 200, 300]
print(l)
# ['a', 100, 200, 300, 'c']
